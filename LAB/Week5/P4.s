;LINEAR SEARCH DISPLAY APPROPRIATE MESSAGE IN I/O PORT

.DATA
A:.WORD 10,20,30,40,50,60,70
KEY:.WORD 90
NOTFOUND:.ASCIZ "NOT FOUND"
FOUND:.ASCIZ "FOUND"

.TEXT

LDR R1,=A
LDR R3,=KEY

MOV R5,#0
LDR R8,[R3]


Loop:LDR R5,[R1],#4
CMP R5,R8
BEQ FIND
ADD R5,R5,#1
CMP R5,#8
BNE Loop
LDR R0,=NOTFOUND
SWI 0X02
B EXIT

FIND:LDR R0,=FOUND
SWI 0X02

EXIT:SWI 0X11